def equal(obj1, obj2):
    return obj1 == obj2

def add(obj1, obj2):
    return obj1 + obj2

def sub(obj1, obj2):
    return obj1 - obj2

def mul(obj1, obj2):
    return obj1 * obj2

def truediv(obj1, obj2):
    return obj1 / obj2

def mod(obj1, obj2):
    return obj1 % obj2

def floordiv(obj1, obj2):
    return obj1 // obj2

def pow(obj1, obj2):
    return obj1 ** obj2

def greater(obj1, obj2):
    return obj1 > obj2

def less(obj1, obj2):
    return obj1 < obj2

def greaterEqual(obj1, obj2):
    return obj1 >= obj2

def lessEqual(obj1, obj2):
    return obj1 <= obj2

def notEqual(obj1, obj2):
    return obj1 != obj2